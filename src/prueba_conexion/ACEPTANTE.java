      public void mostrar(String valor){
    DefaultTableModel modelo=new DefaultTableModel();
    modelo.addColumn("CODIGO");
     modelo.addColumn("RazSoc");
      modelo.addColumn("NumDocIde");
        modelo.addColumn("ApPat");
     modelo.addColumn("ApMat");
      modelo.addColumn("NOMBRE");
        modelo.addColumn("DIRRECCION");
     modelo.addColumn("TELEFONO");
      modelo.addColumn("DocId");
       modelo.addColumn("ESTADO");
      tableaceptante.setModel(modelo);
      String datos[]=new String[10]; 
      String texto="SELECT * FROM ACEPTANTE";
    
    if(valor.equals("")==false)
          texto="SELECT * FROM ACEPTANTE WHERE AcepCod='"+valor+"'";
     
        try {
            Statement st=connection.createStatement();
              ResultSet result=st.executeQuery(texto);
            while(result.next()){
            datos[0]=result.getString(1);
            datos[1]=result.getString(2);
              datos[2]=result.getString(3);
              datos[3]=result.getString(4);
            datos[4]=result.getString(5);
              datos[5]=result.getString(6);
              datos[6]=result.getString(7);
            datos[7]=result.getString(8);
              datos[8]=result.getString(9);
                datos[9]=result.getString(10);
            modelo.addRow(datos);
            }
            tableaceptante.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(ACEPTANTE.class.getName()).log(Level.SEVERE, null, ex);
        }
            
          
    }
