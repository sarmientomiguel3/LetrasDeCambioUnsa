    public void iniciarcombodocumento(){
        
        try {
            Statement st=connection.createStatement();
             ResultSet result=st.executeQuery("SELECT *FROM DOCUMENTOIDENTIDAD");
             
             while(result.next()){
                 cdocumento.addItem(result.getString("DocIdeNom"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(ACEPTANTE.class.getName()).log(Level.SEVERE, null, ex);
        }
            
           cdocumento.setVisible(false);
    }
