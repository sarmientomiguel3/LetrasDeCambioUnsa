    private void ACEPTARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ACEPTARActionPerformed
        // TODO add your handling code here:
         if(opcion==1){
             try {
                 PreparedStatement ps=connection.prepareStatement("INSERT INTO SUCURSALES (SucNomOfi,SucDir,BANCOS_BanCod,"
                         + "LUGARGIRO_LugGirCod,ESTADO_EstCod) VALUES (?,?,?,?,?)");
                  ps.setString(1,txtofi.getText());
         ps.setString(2,txtdir.getText());
           ps.setString(3,txbanco);
         ps.setString(4,txlugargiro);
           ps.setString(5,txestado);
         ps.executeUpdate();
             } catch (SQLException ex) {
                 Logger.getLogger(SUCURSALES.class.getName()).log(Level.SEVERE, null, ex);
             }
             mostrar("");
            }else if(opcion==2){
       mostrar(valbuscar.getText().toString());
        }else if(opcion==3){
             if(txestado.equals(""))
                    txestado=txtestado.getText();
                  if(txlugargiro.equals(""))
                    txlugargiro=txtlug.getText();
                   if(txbanco.equals(""))
                    txbanco=txtbanco.getText();
                  
             try {
                 PreparedStatement pst=connection.prepareStatement("UPDATE SUCURSALES SET SucNomOfi='"
                         +txtofi.getText() +"',SucDir='"+txtdir.getText()+"',BANCOS_BanCod='"+txbanco+
                         "',LUGARGIRO_LugGirCod='"+txlugargiro+ "',ESTADO_EstCod='"+txestado+"' WHERE SucCod='"+
                         txtcod.getText()+"'");
                  pst.executeUpdate();
             } catch (SQLException ex) {
                 Logger.getLogger(SUCURSALES.class.getName()).log(Level.SEVERE, null, ex);
             }
         mostrar("");
            }else if(opcion==4){
             String i ="I";
             try {
                 PreparedStatement pst=connection.prepareStatement("UPDATE SUCURSALES SET ESTADO_EstCod='"+i+
                         "' WHERE SucCod='"
                         +txtcod.getText()+"'");
                  pst.executeUpdate();
             } catch (SQLException ex) {
                 Logger.getLogger(SUCURSALES.class.getName()).log(Level.SEVERE, null, ex);
             }
                 mostrar("");
             }else if(opcion==5){
             String i ="A";
             try {
                 PreparedStatement pst=connection.prepareStatement("UPDATE SUCURSALES SET ESTADO_EstCod='"+i+
                         "' WHERE SucCod='"
                         +txtcod.getText()+"'");
                  pst.executeUpdate();
             } catch (SQLException ex) {
                 Logger.getLogger(SUCURSALES.class.getName()).log(Level.SEVERE, null, ex);
             }
              mostrar("");
             }else if(opcion==6){
             try {
                 String i ="*";
               PreparedStatement pst=connection.prepareStatement("UPDATE SUCURSALES SET ESTADO_EstCod='"+i+
                         "' WHERE SucCod='"
                         +txtcod.getText()+"'");
                  pst.executeUpdate();  
             } catch (SQLException ex) {
                 Logger.getLogger(SUCURSALES.class.getName()).log(Level.SEVERE, null, ex);
             }
             mostrar("");
             }
           clean();
       opcion=0;
    }//GEN-LAST:event_ACEPTARActionPerformed

