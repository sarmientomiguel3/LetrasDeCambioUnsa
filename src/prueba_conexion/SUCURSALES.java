     public void mostrar(String valor){
    DefaultTableModel modelo=new DefaultTableModel();
    modelo.addColumn("CODIGO");
     modelo.addColumn("OFICINA");
      modelo.addColumn("DIRRECCION");
      modelo.addColumn("BANCO");
     modelo.addColumn("LUGAR DE GIRO");
      modelo.addColumn("ESTADO");
       tablesucursales.setModel(modelo);
      String datos[]=new String[6]; 
      String texto="SELECT * FROM SUCURSALES";
    if(valor.equals("")==false)
          texto="SELECT * FROM SUCURSALES WHERE SucCod='"+valor+"'";
        try {
            Statement st=connection.createStatement();
           ResultSet result=st.executeQuery(texto);
            while(result.next()){
            datos[0]=result.getString(1);
            datos[1]=result.getString(2);
              datos[2]=result.getString(3);
               datos[3]=result.getString(4);
            datos[4]=result.getString(5);
              datos[5]=result.getString(6);
               modelo.addRow(datos);
             }
            tablesucursales.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(SUCURSALES.class.getName()).log(Level.SEVERE, null, ex);
        }
         }
     
